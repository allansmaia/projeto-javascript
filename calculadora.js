function somar(a, b) {
    return a + b;
}

function subtrair(a, b) {
    return a - b;
}

function multiplicar(a, b) {
    return a * b;
}

function dividir(a, b) {
    return a / b;
}

function potencia(base, expoente) {
    return Math.pow(base, expoente);
}

function raiz(a, b) {
  return a ** (1 / b);
}


